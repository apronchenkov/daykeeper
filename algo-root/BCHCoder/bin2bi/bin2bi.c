#define BUFFER_SIZE (1024*4)

int main(int argc, char **argv) {
	enum{INIT, IS_READ, IS_WRITE, IS_EOF} state = INIT;
	enum{false, true} ok = true;
	unsigned char in_b[BUFFER_SIZE];
	char out_b[8*BUFFER_SIZE];

	int i;
	
	size_t rec;
	while( ok && IS_EOF != state  ) {
		if( ok ) {
			state = IS_READ;
			rec = fread(in_b, 1, BUFFER_SIZE, stdin);
			ok = (0 == ferror(stdin));
		}
		if( ok ) {
			for(i = 0; i < rec; ++i) 
				memcpy(out_b + i*8, bi[in_b[i]], 8);
		}
		if( ok ) {
			state = IS_WRITE;
			fwrite(out_b, 8*rec, 1, stdout);
			ok = (0 == ferror(stdout));
		}
		if( ok ) {
			state = IS_EOF;
			ok = (0 == feof(stdin));
		}
	}
	if( IS_EOF == state )
		return 0;
	switch( state ) {
	case IS_READ: fprintf(stderr, "%s: read: %s\n", argv[0], strerror(errno)); break;
	case IS_WRITE: fprintf(stderr, "%s: write: %s\n", argv[0], strerror(errno)); break;
	default: assert( 0 );
	}
	return -1;
}
